export function platformShadow(
  color: string,
  offsetY: number,
  radius: number,
  opacity: number,
  elevation: number = 4
): object {
  if (Platform.OS === 'web') {
    return { boxShadow: `0 ${offsetY}px ${radius * 2}px ${color}` };
  }
  return {
    shadowColor: color,
    shadowOffset: { width: 0, height: offsetY },
    shadowOpacity: opacity,
    shadowRadius: radius,
    elevation,
  };
}
